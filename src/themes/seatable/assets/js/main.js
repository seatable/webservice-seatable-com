document.addEventListener('DOMContentLoaded', () => {
    // Increase bottom margin for every last .faq-item element and remove the border
    // This is not possible with plain CSS
    document.querySelectorAll('.faq-item').forEach((item) => {
        if (!item.nextElementSibling?.classList.contains('faq-item')) {
            item.classList.add('mb-6');
            item.classList.remove('border-b');
        }
    });
});
